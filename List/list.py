my_list = [1, 3, 5, 7]

my_list.append(9)
print("After appending--->",my_list) 

my_list.insert(1, 2)
print("After inserting an element--->",my_list)  

my_list.remove(5)
print("After removing an element--->",my_list)  

my_list.pop(2)
print("Pop the element at index--->",my_list)  

my_list.sort()
print("Sorted list--->",my_list)  
